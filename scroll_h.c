unsigned char ucScrollArrowH(void)
{
	unsigned char i;
	unsigned long *pulBuffer;
	const unsigned int *puiArrow;

	if(_isset(xData.ucStatus,DIR_UP))
	{
	  	xArrow.puiArrow=uiArrowHUp;
	}
	else if(_isset(xData.ucStatus,DIR_DOWN))
	{
	  	xArrow.puiArrow=uiArrowHDown;
	}
	else
	{
	  	xArrow.puiArrow=0;
	}

	pulBuffer=xArrow.pulBuffer;
	puiArrow=xArrow.puiArrow;
	//if((xString.ucStr==0)&((xBlock.pucBlock1==ucScrollStringH)|(xBlock.pucBlock2==ucScrollStringH))) puiArrow=0;
	if(puiArrow==0) return 0;

	if(_isset(xData.ucStatus,RUN))
	{
		for(i=0;i<7;i++)
		{
 			if(_isset(xData.ucStatus,DIR_UP))
			{
				if(i==0){ puiArrow=xArrow.puiArrow+7-xArrow.ucShift;}
				if(i==xArrow.ucShift){puiArrow=xArrow.puiArrow;}
			}			
			else if(_isset(xData.ucStatus,DIR_DOWN))
			{
				if(i==0){ puiArrow=xArrow.puiArrow+xArrow.ucShift;}
				if(i==7-xArrow.ucShift){puiArrow=xArrow.puiArrow;}
			}
 				
			*pulBuffer=(unsigned long)(*puiArrow);
			//if(i>7)	{ *pulBuffer<<=16;}	
			++puiArrow;
			++pulBuffer;
		}
	}
	else
	{
		for(i=0;i<7;i++)
		{
			*pulBuffer=(unsigned long)(*puiArrow); 
			//if(i>7)	{ *pulBuffer<<=16;}
			++puiArrow;
			++pulBuffer;
		}		
	}

	if(ulArrowDelay == 0)
	{
		if(_isset(xData.ucStatus,RUN))
		{
			++xArrow.ucShift;
			if(xArrow.ucShift>15) xArrow.ucShift = 0;
			ulArrowDelay = ARROW_SPEED;
		}
		else
		{
			xArrow.ucShift = 0;
		}	
	} 
	return 1;		 
}
