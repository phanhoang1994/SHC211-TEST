unsigned char ucScrollNumberH(void)
{
	unsigned char i;
	unsigned long *pulBuffer;
	const unsigned int *puiData = 0;
	const unsigned int *puiCur;
	const unsigned int *puiNext;

	pulBuffer=xNumber.pulBuffer;

	puiCur=puiNumberH_Table[xNumber.ucCur];
	puiNext=puiNumberH_Table[xNumber.ucNext];

	if(pulBuffer==0) return 0;
	if((puiCur==0)&(puiNext==0)) return 0;

	if(puiCur==puiNext)
	{
		for(i=0;i<16;i++)
		{
			*pulBuffer=(unsigned long)(*puiCur);
			//if(i>7)	{ *pulBuffer<<=16;}	
			++pulBuffer;
			++puiCur;
		}
	}
	else
	{
		for(i=0;i<16;i++)
		{
			if(_isset(xData.ucStatus,DIR_UP))
			{
				if((i+1)>xNumber.ucShift){ puiData=puiCur+i-xNumber.ucShift;}
				if((i<(xNumber.ucShift))&((i+3)>xNumber.ucShift)){ puiData=0;}
				if((i+2)<xNumber.ucShift){ puiData=puiNext+18+i-xNumber.ucShift;}
			}
			else
			{
				if((i+xNumber.ucShift)<16){ puiData=puiCur+xNumber.ucShift+i;}
				if(((i+xNumber.ucShift)>15)&((i+xNumber.ucShift)<18)){ puiData=0;}
				if((i+xNumber.ucShift)>17){ puiData=puiNext+i+xNumber.ucShift-18;}
			}
 			
			if(puiData!=0){*pulBuffer=(unsigned long)*puiData;}else{*pulBuffer=0;};				
			//if(i>7)	{ *pulBuffer<<=16;}	
			++pulBuffer;
		}
	}
	
	if(xNumber.ucCur!=xNumber.ucNext)
	{		
		if(ulNumberDelay == 0)
		{
			++xNumber.ucShift;
			if(xNumber.ucShift>17) 
			{
				xNumber.ucCur=xNumber.ucNext;
				xNumber.ucShift = 0;
			}
			ulNumberDelay = NUMBER_SPEED;
		}
	}
	return 1;		 
} 

void vScrollH(void)	//Ngang
{
	unsigned char i;
	xStringHandler xStr;

	/* Broadcast Frame: 8byte
		BYTE0: 	NumOfFloor
		BYTE1: 	Display
		BYTE2: 	Status 
				Bit0: Up
				Bit1: Down
				Bit2: Run
				Bit3: Earthquake
				Bit4: Fireman
				Bit5: Overload
				Bit6: Busy
				Bit7: Big digits

		BYTE3: 	Current floor
		BYTE4: 	Unused
		BYTE5: 	Unused
		BYTE6: 	Unused
		BYTE7: 	Unused
	*/

	xStr.ucStr = 0;
	
	// Display number
	if(_isset(xData.ucStatus,EARTHQUAKE)|_isset(xData.ucStatus,FIREMAN)|
		_isset(xData.ucStatus,OVERLOAD) | _isset(xData.ucStatus, NHB))
	{
		//xBlock.pucBlock1=ucScrollStringH;				
		xBlock.pucBlock2=ucScrollNumberH;
		xString.pulBuffer=ulBlock1Data;
		xNumber.pulBuffer=ulBlock2Data;
		xArrow.pulBuffer=0;

		//if(_isset(xData.ucStatus,BUSY)){xStr.ucStr=9;}
		if(_isset(xData.ucStatus,NHB)){xStr.ucStr=4;}
		if(_isset(xData.ucStatus,OVERLOAD)){xStr.ucStr=5;}
		if(_isset(xData.ucStatus,EARTHQUAKE)){xStr.ucStr=7;}
		if(_isset(xData.ucStatus,FIREMAN)){xStr.ucStr=3;}

		i=0;
		while((xData.ucLed7Seg!=xNumberCodeH[i].ucLed7Seg) & (i<NUM_LEDCODE)){++i;}; //Search ledcode
		if(i<NUM_LEDCODE){xNumber.ucNext=xNumberCodeH[i].ucNumber;};			
	}
	else
	{
		xString.pulBuffer=ulBlock2Data;
		xNumber.pulBuffer=ulBlock2Data;
		xArrow.pulBuffer=ulBlock1Data;

		i=0;
		while((xData.ucLed7Seg!=xNumberCodeH[i].ucLed7Seg) & (i<NUM_LEDCODE)){++i;}; //Search ledcode

		if(i<NUM_LEDCODE)
		{
			xBlock.pucBlock2=xNumberCodeH[i].pucBlock1;
			xBlock.pucBlock1=xNumberCodeH[i].pucBlock2;
			
			xNumber.ucNext=xNumberCodeH[i].ucNumber;
			xStr.ucStr=xNumberCodeH[i].ucStr;
		}		
	}

	if(xString.ucStr!=xStr.ucStr) 
	{
		xString.ucStr=xStr.ucStr;
		xString.ucShift=0;
	}
}

